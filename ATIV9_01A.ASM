TITLE PROGRAMA EXEMPLO PARA MANIPULAÇÃO DE VETORES USANDO BX
.MODEL SMALL
.STACK 100H
.DATA
VETOR DB 1, 1, 1, 2, 2, 2
.CODE
MAIN PROC
;REALIZA A LEITURA DE .DATA
MOV AX, @DATA
MOV DS,AX       
;ZERA DL
XOR DL, DL
;REALIZA A LEITURA DO VETOR INSERIDO
LEA BX, VETOR
;ENTRA NO PROCEDIMENTO VOLTA
CALL VOLTA
;FINALIZA O PROGRAMA APÓS VOLTAR DO PROCEDIMENTO VOLTA
MOV AH,4CH
INT 21H ;saida para o DOS
MAIN ENDP

VOLTA PROC
;ATRIBUI 6 A CX
MOV CX,6
;IRÁ IMPIMIR O VETOR LIDO EM .DATA
IMPRESSÃO:
;PASSA O QUE TEM NO ENDEREÇO DE BX PARA O DL, PARA SER IMPRESSO
MOV DL, [BX]
;SOMA UM EM BX PARA LER O PROXIMO END DO VETOR
INC BX
;ADICIONA 30H AO NUMERO LIDO DO ENDEREÇO DE BX PARA SER IMPRESSO COMO SEU CORRESPONDENTE NA TABELA ASCII
ADD DL, 30H
;IMPRIME OS CARACTERES DO VETOR
MOV AH, 02
INT 21H
;COLOCA IMPRESSÃO EM LOOP ATÉ QUE CX SEJA ZERADO
LOOP IMPRESSÃO
;RETORNA AO PROGRAMA PRINCIPAL
RET
VOLTA ENDP
END MAIN