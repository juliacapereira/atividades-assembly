TITLE prova
.model small
.stack 100h
.DATA
MSG1 DB "DIGITE O MULTIPLICANDO:$"
MSG2 DB 13,10,"DIGITE O MULTIPLICADOR:$"
MSG3 DB 13,10,"O RESULTADO E:$"
.CODE
    MAIN PROC
        MOV AX,@DATA
        MOV DS,AX
        ;COMEÇA CH (CONADOR PARA O MULTIPLICADOR) ZERADO 
        XOR CH, CH
        
        ;IMPRIME NA TELA A MSG1
        MOV AH,9
        MOV DX, OFFSET MSG1
        INT 21H

        ;LE O CARACTER INSERIDO
        MOV AH,1
        INT 21H
        ;MOVE O CARACTER PARA BL(MULTIPLICANDO)
        MOV BL,AL
        ;SUBSTITUI O SUB BL,30H PARA REALIZAR A CONTA CORRETA DE ACORDO COM A TABELA ASCII
        AND bl,0FH

        ;IMPRIME NA TELA A MSG2
        MOV AH,9
        MOV DX, OFFSET MSG2
        INT 21H

        ;LE O CARACTER INSERIDO
        MOV AH,1
        INT 21H
        ;JOGA O CARACTER PARA CL(MULTIPLICADOR)
        MOV CL,AL
        ;SUBSTITUI O SUB CL,30H PARA REALIZAR A CONTA CORRETA DE ACORDO COM A TABELA ASCII
        AND CL,0FH

        ;JOGA CL(MULTIPLICADOR) PARA CH QUE VAI SER O CONTADOR DO LOOP
        MOV CH,CL

        ;JOGA BL (MULTIPLICANDO) PARA DL (QUE VAI SER O N° DO MULTIPLICADOR FIXO PARA SOMAR ENQUANTO O LOOP RODAR)
        MOV DH, BL
        ;ZERA O BL PARA INICIAR A SOMA
        XOR BL,BL
        MULTIPLICACAO:
        ;ADICIONA O MULTIPLICADOR FIXO(VALOR INICIAL) AO NOVO NUMERO
        ADD BL,DH
        ;FAZ A FUNÇÃO ENTRAR EM LOOP
        LOOP MULTIPLICACAO

        ;ACRESCENTA 30H A BL-RESULTADO PARA ENCONTRAR O CORRESPONDENTE NA TABELA ASCII
        OR BL,30H

        ;IMPRIME A MSG3 NA TELA
        MOV AH,9
        MOV DX, OFFSET MSG3
        INT 21H
    
        ;IMPRIME DL (QUE RECEBE BL, QUE É O RESULTADO)
        MOV AH,2
        MOV DL,BL
        INT 21H

        ;FINALIZA O PROGRAMA
        MOV AH,4ch
        INT 21H
        MAIN ENDP
        END MAIN 