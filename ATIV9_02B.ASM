TITLE PROGRAMA EXEMPLO PARA MANIPULAÇÃO DE VETORES USANDO SI ou DI
.MODEL SMALL
.STACK 100H
.DATA
VETOR DB 1, 1, 1, 2, 2, 2
.CODE
MAIN PROC
MOV AX, @DATA
MOV DS,AX
XOR DL, DL
MOV CX,6
XOR DI, DI
CALL IMPRESSAO
MOV AH,4CH
INT 21H ;saida para o DOS
MAIN ENDP
IMPRESSAO PROC
VOLTA:
MOV DL, VETOR[DI];LE CADA POSIÇÃO DO VETOR INDIVIDUALMENTE E PASSA-AS PARA DL CONFORME O ENDEREÇO DENTRO DE DI (EX. DI COMEÇA ZERADO, ENTÃO IMPRIME O PRIMEIRO 1, 
INC DI ;QUE TEM O ENDEREÇO 0, QUANDO SOMA 1, ELE LÊ O PROXIMO 1, QUE TEM A POSIÇÃO 1 (LEMBRANDO QUE PPOSIÇÃO DE VETORES É A PARTIR DO 0))
;ADICIONA 30H AO NUMERO LIDO DO ENDEREÇO DE DI PARA SER IMPRESSO COMO SEU CORRESPONDENTE NA TABELA ASCII
ADD DL, 30H
;IMPRIME OS CARACTERES DO VETOR
MOV AH, 02
INT 21H
;COLOCA VOLTA EM LOOP ATÉ QUE CX SEJA ZERADO
LOOP VOLTA
RET

IMPRESSAO ENDP

END MAIN