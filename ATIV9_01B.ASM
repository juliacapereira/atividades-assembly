.MODEL SMALL
.STACK 100H
.DATA
VETOR DB 1, 1, 1, 2, 2, 2
.CODE
MAIN PROC
;REALIZA A LEITURA DE .DATA
MOV AX, @DATA
MOV DS,AX
;ZERA DL
XOR DL, DL
;ATRIBUI 6 A CX PARA O LOOP
MOV CX,6
;ZERA BX
XOR BX, BX
;ENTRA NO PROCEDIMENTO IMPRESSAO
CALL IMPRESSAO
;FINALIZA O PROGRAMA
MOV AH,4CH
INT 21H ;saida para o DOS
MAIN ENDP

IMPRESSAO PROC
VOLTA:
MOV DL, VETOR[BX] ;LE CADA POSIÇÃO DO VETOR INDIVIDUALMENTE E PASSA-AS PARA DL CONFORME O ENDEREÇO DENTRO DE BX (EX. BX COMEÇA ZERADO, ENTÃO IMPRIME O PRIMEIRO 1, 
INC BX  ;QUE TEM O ENDEREÇO 0, QUANDO SOMA 1, ELE LÊ O PROXIMO 1, QUE TEM A POSIÇÃO 1 (LEMBRANDO QUE PPOSIÇÃO DE VETORES É A PARTIR DO 0))
;ADICIONA 30H AO NUMERO LIDO DO ENDEREÇO DE BX PARA SER IMPRESSO COMO SEU CORRESPONDENTE NA TABELA ASCII
ADD DL, 30H
;IMPRIME O CARACTER DA POSIÇÃO CORRESPONDENTE AO VALOR DE BX
MOV AH, 02
INT 21H
;COLOCA VOLTA EM LOOP ATÉ QUE CX SEJA 0
LOOP VOLTA
;RETORNA AO MAIN
RET
IMPRESSAO ENDP
END MAIN