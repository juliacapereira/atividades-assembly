TITLE PROGRAMA EXEMPLO PARA MANIPULAÇÃO DE VETORES USANDO SI ou DI
.MODEL SMALL
.STACK 100H
.DATA
VETOR DB 1, 1, 1, 2, 2, 2
.CODE
MAIN PROC
;REALIZA A LEITURA DE .DATA
MOV AX, @DATA
MOV DS,AX
;ZERA DL
XOR DL, DL
;ATRIBUI 6 A CX PARA O LOOP
MOV CX,6
;REALIZA A LEITURA DO VETOR INSERIDO
LEA SI, VETOR
;ENTRA NO PROCEDIMENTO IMPRESSAO
CALL IMPRESSAO
;FINALIZA TUDO
MOV AH,4CH
INT 21H ;saida para o DOS
MAIN ENDP

IMPRESSAO PROC
VOLTA:
;PASSA O QUE TEM NO ENDEREÇO DE SI PARA O DL, PARA SER IMPRESSO
MOV DL, [SI]
;SOMA UM EM BX PARA LER O PROXIMO END DO VETOR
INC SI
;ADICIONA 30H AO NUMERO LIDO DO ENDEREÇO DE BX PARA SER IMPRESSO COMO SEU CORRESPONDENTE NA TABELA ASCII
ADD DL, 30H
;IMPRIME OS CARACTERES DO VETOR
MOV AH, 02
INT 21H
;COLOCA VOLTA EM LOOP ATÉ QUE CX SEJA ZERADO
LOOP VOLTA
RET
IMPRESSAO ENDP
END MAIN